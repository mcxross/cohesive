package com.mcxross.cohesive.mellow

/**
 * Enum representing the state of a card.
 *
 * This enum can be used to specify the state of a card, for example, to determine whether it should
 * be displayed with a flat or raised appearance.
 */
enum class CardState {
  /** Flat state. */
  FLAT,
  /** Raised state. */
  RAISED
}
